public class TipoGlobal {

  public static byte _variable;
  public static short $variable;
  public static int variable;
  public static long __variable;
  public static double _$variable;

  public static char VarChart;
  public static boolean var;
  public static String VarString;

  public static void main (String[]args) {

      System.out.println("Tipo Byte = " + _variable);
      System.out.println("Tipo Short = " + $variable);
      System.out.println("Tipo Integer = " + variable);
      System.out.println("Tipo Entero Largo = " + __variable);
      System.out.println("Tipo Real Double = " + _$variable);

      System.out.println("Tipo Character = " + VarChart);
      System.out.println("Tipo Booleano = " + var);
      System.out.println("Tipo String = " + VarString);

    }
}
