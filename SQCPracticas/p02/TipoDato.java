public class TipoDato {
  public static void main (String args[]) {
    byte _variable = 127;
    short $variable = 32767;
    int variable = 2147483647;
    long __variable = 9223372036854775807L;
    String _variable_ = null;
    String VarString = "hola java";

    System.out.println("Tipo Byte = " + _variable);
    System.out.println("Tipo Short = " + $variable);
    System.out.println("Tipo Integer = " + variable);
    System.out.println("Tipo Entero Largo = " + __variable);
    System.out.println("Tipo String NULA = " + _variable_);
    System.out.println("Tipo String = " + VarString);
  }
}
